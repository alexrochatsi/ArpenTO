package br.com.datasind.gerente;

import br.com.datasind.aplicacao.ApplicationException;



public class UsuarioInativoException extends ApplicationException {

	private static final long serialVersionUID = 1L;

	/**
	 * @param message
	 * @param cause
	 */
	public UsuarioInativoException(String message, Throwable cause) {
		super(message, cause);
	}

	/**
	 * @param message
	 */
	public UsuarioInativoException(String message) {
		super(message);
	}

	/**
	 * @param cause
	 */
	public UsuarioInativoException(Throwable cause) {
		super(cause);
	}

	/**
	 * 
	 */
	public UsuarioInativoException() {
		super();
	}

}
